<?php

//Producto.php
class Product {

    public function __construct($id, 
    $categoria, 
    $nombre, 
    $precio, 
    $talla, 
    $color, 
    $stock, 
    $ajuste, 
    $sexo, 
    $descripcion, 
    $altura, 
    $deporte, 
    $oferta)  {
        $this->id = id;
        $this->categoria = categoria;
        $this->nombre = nombre;
        $this->precio = precio;
        $this->talla = talla;
        $this->color = color;
        $this->stock = stock;
        $this->ajuste = ajuste;
        $this->sexo = sexo;
        $this->descripcion = descripcion;
        $this->altura = altura;
        $this->deporte = deporte;
        $this->oferta = oferta;
    }




}



?>